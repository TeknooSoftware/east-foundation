{
    /*
     * Adapter to plug a template engine, like Twig to and RenderingInterface endpoint
     *
     * @param array<string, mixed> $parameters
     */
    public function render(
        PromiseInterface $promise,
        string $view,
        array $parameters = []
    ): EngineInterface;
}
